// Helper function to get featured articles
export const getFeaturedArticles = () => {
  return blogArticles.filter(article => article.featured);
};

// Helper function to get all articles except featured
export const getNonFeaturedArticles = () => {
  return blogArticles.filter(article => !article.featured);
};

// Helper function to get article by slug
export const getArticleBySlug = (slug) => {
  return blogArticles.find(article => article.slug === slug);
};

// Helper function to get related articles
export const getRelatedArticles = (articleId) => {
  const article = blogArticles.find(a => a.id === articleId);
  if (!article || !article.relatedArticles) return [];
  
  return article.relatedArticles.map(id => 
    blogArticles.find(a => a.id === id)
  ).filter(Boolean);
};

// Helper function to get articles by category
export const getArticlesByCategory = (category) => {
  return blogArticles.filter(article => article.category === category);
};
